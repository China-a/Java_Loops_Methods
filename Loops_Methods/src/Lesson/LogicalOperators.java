// An example of the NOT (!) operator 
    class LogicalOperators {
        public static void main(String[] args) {
            int number = 21;

            if (!(number > 13)) {
                System.out.println("The number is not greater than 13.");
            } else {
                System.out.println("The number is greater than or equal to 13.");
        }

    }
}